package com.mpgl.vo;

/**
 * 活动VO
 * 
 * @author Administrator
 * 
 */
public class ActionVo {

	private String id;

	private String name;

	private String type;

	private String chance_id;

	private String contact_id;

	private String action_cost;

	private String start_date;

	private String end_date;

	private String remark;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getChance_id() {
		return chance_id;
	}

	public void setChance_id(String chance_id) {
		this.chance_id = chance_id;
	}

	public String getContact_id() {
		return contact_id;
	}

	public void setContact_id(String contact_id) {
		this.contact_id = contact_id;
	}

	public String getAction_cost() {
		return action_cost;
	}

	public void setAction_cost(String action_cost) {
		this.action_cost = action_cost;
	}

	public String getStart_date() {
		return start_date;
	}

	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}

	public String getEnd_date() {
		return end_date;
	}

	public void setEnd_date(String end_date) {
		this.end_date = end_date;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
